package com.mycompany.stringconcatenation;

public class StringConcatenation {
    public static void main(String[] args) {
        String strValue1;
        String strValue2;
        String strValue3;
        strValue1 = "Pro";
        strValue2 = "gram";
        strValue3 = "ming";
        String strConcatenated = strValue1 + strValue2 + strValue3;
        
        System.out.println(strConcatenated);
    }
}
